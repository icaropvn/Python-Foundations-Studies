'''
Exemplo 013 - Lado A
"Estrutura de Repetição 'for'"
'''

for i in range(1, 6): # Comando repetindo-se 5 vezes (1 - 5)
    if i == 5:
        print('Oi\n')
    else:
        print('Oi')

for i in range(0, 6): # Comando repetindo-se 6 vezes (0 - 5)
    if i == 5:
        print('Abueloeir Final\n')
    else:
        print('Abueloier')

for i in range(10, 0, -1): # Comando repetindo-se 10 vezes, diminuindo a cada vez em 1 (10 - 1)
    print(i)

print('\nFim')